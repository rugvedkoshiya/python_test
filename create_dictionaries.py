# There are two ways to create dictionaries

# First Way...
d1 = {'name' : 'Rugved', 'age' : 18}
print(d1) # Output : {'name': 'Rugved', 'age': 18}
print(type(d1)) # Output : <class 'dict'>

# Second Way...
d2 = dict(name = 'Rugved', age = 18)
print(d2) # Output : {'name': 'Rugved', 'age': 18}
print(type(d2)) # Output : <class 'dict'>
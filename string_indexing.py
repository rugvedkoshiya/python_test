# String Indexing

name = "Rugved"

# Position (Index Number)
# R  =  0 , -6
# u  =  1 , -5
# g  =  2 , -4
# v  =  3 , -3
# e  =  4 , -2
# d  =  5 , -1

print(name[0]) # Output : R
print(name[1]) # Output : u
print(name[2]) # Output : g
print(name[3]) # Output : v
print(name[4]) # Output : e
print(name[5]) # Output : d

# Reverse Indexing

print(name[-6]) # Output : R
print(name[-5]) # Output : u
print(name[-4]) # Output : g
print(name[-3]) # Output : v
print(name[-2]) # Output : e
print(name[-1]) # Output : d